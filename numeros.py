numero_uno = 10
numero_dos = 4

resultado = numero_uno + numero_dos
print("Suma", resultado)

resultado = numero_uno - numero_dos
print("Resta", resultado)

resultado = numero_uno * numero_dos
print("Multiplicacion", resultado)

resultado = numero_uno / numero_dos #Retorna un numero flotante
print("Division", resultado)

resultado = numero_uno // numero_dos #Retorna un numero entero
print("Division", resultado)

resultado = numero_uno ** numero_dos #Eleva el n1 al n2
print("Potencia", resultado)
